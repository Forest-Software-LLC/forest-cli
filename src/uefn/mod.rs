//! UEFN (Verse) platform knowledge.
//!
//! Everything Verse-specific the CLI needs: project discovery, the scope →
//! Verse-identifier mapping, package-name validation, marker-file
//! generation, the running-editor probe, and the root-definition lint.
//! Identifier rules come from the shared contract (see src/contracts.rs);
//! the layout/marker semantics are compiler-verified in forest-backend
//! docs/uefn-adapter.md (§4–§7).

